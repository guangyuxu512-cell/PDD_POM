"""售后决策引擎。"""
from __future__ import annotations

import re
from typing import Any


class 售后决策引擎:
    """售后决策引擎，负责按可用按钮决定处理路径。"""

    @staticmethod
    def _提取金额(值: Any) -> float:
        if isinstance(值, (int, float)):
            return float(值)
        匹配 = re.search(r"(\d+(?:\.\d+)?)", str(值 or ""))
        return float(匹配.group(1)) if 匹配 else 0.0

    def _找按钮(self, 可用按钮: list[str], 优先级列表: list[str]) -> str:
        for 关键词 in 优先级列表:
            for 按钮 in 可用按钮:
                if 关键词 in 按钮:
                    return 按钮
        return ""

    async def 决策(self, 详情: dict, 售后配置: dict, 队列记录: dict) -> dict:
        售后类型 = str(详情.get("售后类型", "")).strip()
        可用按钮 = list(详情.get("可用按钮列表") or [])
        结果 = {
            "操作": "人工处理",
            "目标按钮": "",
            "备选按钮": [],
            "弹窗偏好": dict(售后配置.get("弹窗偏好") or {}),
            "需要备注": False,
            "备注内容": "",
            "需要飞书通知": False,
            "飞书通知内容": "",
            "人工原因": "",
            "下次处理天数": None,
            "需要微信通知": False,
            "微信通知内容": "",
            "需要飞书写入": False,
        }

        if "退款" in 售后类型 and "退货" not in 售后类型:
            if self._找按钮(可用按钮, ["同意退款", "同意拒收退款", "同意拒收后退款", "拒绝"]):
                return self._决策_退款(详情, 可用按钮, 售后配置, 队列记录, 结果)

        if self._找按钮(可用按钮, ["同意拒收退款", "同意拒收后退款"]):
            return self._决策_拒收退款(详情, 可用按钮, 售后配置, 结果)

        if self._找按钮(可用按钮, ["同意退货"]):
            结果["操作"] = "同意退货"
            结果["目标按钮"] = self._找按钮(可用按钮, ["同意退货"])
            return 结果

        if self._找按钮(可用按钮, ["同意退款"]):
            return self._决策_退货后退款(详情, 可用按钮, 售后配置, 队列记录, 结果)

        结果["操作"] = "跳过"
        return 结果

    def _决策_拒收退款(
        self,
        详情: dict,
        可用按钮: list[str],
        售后配置: dict,
        结果: dict,
    ) -> dict:
        """有“同意拒收退款”按钮时，进入物流拦截通知路径。"""
        if not bool(售后配置.get("拒收退款_启用", True)):
            结果["操作"] = "人工处理"
            结果["人工原因"] = "拒收退款自动处理已关闭"
            return 结果

        if not bool(售后配置.get("拒收退款_需要检查物流", True)):
            结果["操作"] = "同意拒收退款"
            结果["目标按钮"] = self._找按钮(可用按钮, ["同意拒收退款", "同意拒收后退款"])
            return 结果

        物流状态 = str(详情.get("物流最新状态", "")).strip()
        订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""
        快递单号 = str(详情.get("发货快递单号", "")).strip()

        结果["操作"] = "人工处理"
        结果["需要微信通知"] = True
        结果["需要飞书通知"] = True

        if "签收" in 物流状态:
            结果["人工原因"] = "已签收，需拦截退回"
            结果["微信通知内容"] = f"订单{订单号} 快递{快递单号} 已签收，请拦截退回"
            结果["飞书通知内容"] = 结果["微信通知内容"]
            结果["需要飞书写入"] = True
            return 结果

        结果["人工原因"] = "未签收，通知拦截"
        结果["微信通知内容"] = f"订单{订单号} 快递{快递单号} 未签收，请拦截/改地址退回"
        结果["飞书通知内容"] = 结果["微信通知内容"]
        return 结果

    def _决策_退款(
        self,
        详情: dict,
        可用按钮: list[str],
        售后配置: dict,
        队列记录: dict,
        结果: dict,
    ) -> dict:
        """仅退款场景决策。"""
        if not bool(售后配置.get("仅退款_启用", False)):
            结果["操作"] = "人工处理"
            结果["人工原因"] = "仅退款自动处理已关闭"
            return 结果

        金额 = self._提取金额(详情.get("退款金额", 0))
        自动同意金额上限 = self._提取金额(
            售后配置.get("仅退款_自动同意金额上限", 10)
        )
        需要拒绝 = bool(售后配置.get("仅退款_需要拒绝", False))
        最大拒绝次数 = int(售后配置.get("仅退款_最大拒绝次数", 3) or 3)
        有图片转人工 = bool(售后配置.get("仅退款_有图片转人工", True))
        拒收退回自动同意 = bool(售后配置.get("仅退款_拒收退回自动同意", True))
        物流状态 = str(详情.get("物流最新状态", "")).strip()
        拒绝次数 = int(队列记录.get("拒绝次数", 0) or 0)
        订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""
        备注模板 = dict(售后配置.get("备注模板") or {})

        if 有图片转人工 and bool(详情.get("有售后图片", False)):
            结果["操作"] = "人工处理"
            结果["人工原因"] = "有售后图片需人工查看"
            结果["需要备注"] = True
            结果["备注内容"] = str(备注模板.get("人工") or "转人工处理")
            结果["需要飞书通知"] = True
            结果["飞书通知内容"] = f"订单{订单号}有售后图片，请人工审核"
            return 结果

        if 金额 <= 自动同意金额上限:
            目标按钮 = self._找按钮(可用按钮, ["同意退款", "同意拒收退款", "同意拒收后退款"])
            if 目标按钮:
                结果["操作"] = "同意退款"
                结果["目标按钮"] = 目标按钮
                return 结果

        if 拒收退回自动同意 and ("拒收" in 物流状态 or "退回" in 物流状态):
            目标按钮 = self._找按钮(可用按钮, ["同意拒收退款", "同意拒收后退款", "同意退款"])
            if 目标按钮:
                结果["操作"] = "同意拒收退款" if "拒收" in 目标按钮 else "同意退款"
                结果["目标按钮"] = 目标按钮
                return 结果

        if 需要拒绝 and 拒绝次数 < 最大拒绝次数:
            目标按钮 = self._找按钮(可用按钮, ["拒绝"])
            if 目标按钮:
                结果["操作"] = "拒绝"
                结果["目标按钮"] = 目标按钮
                结果["需要备注"] = True
                结果["备注内容"] = str(备注模板.get("拒绝") or "系统拒绝第{n}次").replace(
                    "{n}",
                    str(拒绝次数 + 1),
                )
                return 结果

        if 需要拒绝 and 拒绝次数 >= 最大拒绝次数:
            结果["操作"] = "跳过"
            结果["人工原因"] = f"已拒绝{最大拒绝次数}次，不再自动处理"
            结果["需要飞书通知"] = True
            结果["飞书通知内容"] = f"订单{订单号}已拒绝{最大拒绝次数}次，请人工跟进"
            return 结果

        结果["操作"] = "人工处理"
        结果["人工原因"] = f"仅退款场景未命中自动处理规则，金额={金额}"
        结果["需要备注"] = True
        结果["备注内容"] = str(备注模板.get("人工") or "转人工处理")
        return 结果

    def _决策_退货后退款(
        self,
        详情: dict,
        可用按钮: list[str],
        售后配置: dict,
        队列记录: dict,
        结果: dict,
    ) -> dict:
        """有“同意退款”按钮时，按退货物流状态决定后续动作。"""
        del 队列记录

        退货物流 = dict(详情.get("退货物流信息") or {})
        有退货物流 = bool(退货物流.get("有退货物流"))
        if not 有退货物流:
            结果["操作"] = "人工处理"
            结果["人工原因"] = "无退货物流单号，需更改"
            return 结果

        白名单 = list(售后配置.get("退货物流白名单") or [])
        等待时间 = dict(
            售后配置.get("退货等待时间")
            or {"刚发出": 3, "中途运输": 1, "到达目的市": 0.25}
        )
        需要入库校验 = bool(售后配置.get("需要入库校验", False))
        自动退款上限 = float(售后配置.get("自动退款金额上限", 50) or 50)

        所有地区词: list[str] = []
        for 规则 in 白名单:
            所有地区词.extend(list(规则.get("地区关键词") or []))

        轨迹全文 = str(退货物流.get("轨迹全文", ""))
        物流阶段 = self._判断物流阶段(轨迹全文, 所有地区词)

        if 物流阶段 == "刚发出":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间.get("刚发出", 3)
            return 结果

        if 物流阶段 == "中途运输":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间.get("中途运输", 1)
            return 结果

        if 物流阶段 == "到达目的市":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间.get("到达目的市", 0.25)
            return 结果

        if 物流阶段 in ("派送中", "已签收"):
            匹配 = self._匹配白名单(退货物流, 白名单)
            if not 匹配["派件人匹配"]:
                结果["操作"] = "人工处理"
                结果["人工原因"] = "派件人不在白名单"
                return 结果

            if 需要入库校验:
                订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""
                结果["操作"] = "等待验货"
                结果["需要备注"] = True
                结果["备注内容"] = "退回件已到，待入库校验"
                结果["需要飞书通知"] = True
                结果["飞书通知内容"] = f"订单{订单号} 退回件到达，请入库校验后在前端确认"
                return 结果

            金额 = self._提取金额(详情.get("退款金额", 0))
            if 金额 <= 自动退款上限:
                结果["操作"] = "同意退款"
                结果["目标按钮"] = self._找按钮(可用按钮, ["同意退款"])
                return 结果

            订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""
            结果["操作"] = "等待验货"
            结果["需要备注"] = True
            结果["备注内容"] = f"退回件已到，金额{金额}元超限{自动退款上限}，待验货"
            结果["需要飞书通知"] = True
            结果["飞书通知内容"] = f"订单{订单号} 金额{金额}超限，请验货后确认"
            return 结果

        结果["操作"] = "人工处理"
        结果["人工原因"] = f"物流状态未知: {轨迹全文[:80]}"
        return 结果

    @staticmethod
    def _判断物流阶段(轨迹全文: str, 目的市关键词: list[str]) -> str:
        if not 轨迹全文:
            return "未知"

        if "签收" in 轨迹全文 or "已签收" in 轨迹全文:
            return "已签收"

        if "派件" in 轨迹全文 or "派送" in 轨迹全文 or "正在为您" in 轨迹全文:
            return "派送中"

        for 关键词 in 目的市关键词:
            if (
                f"到达{关键词}" in 轨迹全文
                or f"到{关键词}" in 轨迹全文
                or f"{关键词}市" in 轨迹全文
            ):
                return "到达目的市"

        if "运输" in 轨迹全文 or "转运" in 轨迹全文 or "发往" in 轨迹全文:
            return "中途运输"

        if "揽收" in 轨迹全文 or "已发出" in 轨迹全文 or "已揽件" in 轨迹全文:
            return "刚发出"

        return "未知"

    @staticmethod
    def _匹配白名单(退货物流: dict, 白名单: list[dict]) -> dict:
        """匹配退货物流是否命中白名单。"""
        快递公司 = str(退货物流.get("退货快递公司", ""))
        轨迹全文 = str(退货物流.get("轨迹全文", ""))
        派件人 = str(退货物流.get("派件人", ""))
        网点 = str(退货物流.get("网点", ""))
        合并文本 = f"{轨迹全文} {派件人} {网点}"

        for 规则 in 白名单:
            规则快递 = str(规则.get("快递公司", "*"))
            if 规则快递 != "*" and 规则快递 not in 快递公司:
                continue

            地区匹配 = any(关键词 in 合并文本 for 关键词 in list(规则.get("地区关键词") or []))
            派件人匹配 = any(名字 in 合并文本 for 名字 in list(规则.get("派件人") or []))
            if 地区匹配 or 派件人匹配:
                return {
                    "地区匹配": 地区匹配,
                    "派件人匹配": 派件人匹配,
                    "匹配的规则": 规则,
                }

        return {"地区匹配": False, "派件人匹配": False, "匹配的规则": None}


__all__ = ["售后决策引擎"]
