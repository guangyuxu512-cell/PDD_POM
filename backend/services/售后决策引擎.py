"""售后决策引擎。"""
from __future__ import annotations

import re
from typing import Any

from backend.services.规则服务 import 规则服务
from backend.services.售后队列服务 import 售后队列服务


class 售后决策引擎:
    """基于详情页抓取结果、规则配置和队列状态生成售后操作决策。"""

    def __init__(self):
        self._规则服务 = 规则服务()
        self._队列服务 = 售后队列服务()

    @staticmethod
    def _提取金额(值: Any) -> float:
        if isinstance(值, (int, float)):
            return float(值)
        匹配 = re.search(r"(\d+(?:\.\d+)?)", str(值 or ""))
        return float(匹配.group(1)) if 匹配 else 0.0

    async def 决策(self, 详情: dict, 规则配置: dict, 队列记录: dict) -> dict:
        """根据详情页信息做出处理决策。"""
        售后类型 = str(详情.get("售后类型", "")).strip()
        可用按钮 = list(详情.get("可用按钮列表") or [])

        结果 = {
            "操作": "人工处理",
            "目标按钮": "",
            "备选按钮": [],
            "弹窗偏好": dict(规则配置.get("弹窗偏好") or {}),
            "需要备注": False,
            "备注内容": "",
            "需要飞书通知": True,
            "飞书通知内容": "",
            "人工原因": "",
        }

        if "退货退款" in 售后类型:
            if not self._有操作按钮(可用按钮):
                结果["操作"] = "跳过"
                结果["人工原因"] = "详情页无操作按钮，可能已被人工处理"
                结果["需要飞书通知"] = False
                return 结果
            return await self._决策_退货退款(详情, 可用按钮, 规则配置, 结果)

        if "退款" in 售后类型 and "退货" not in 售后类型:
            if not self._有操作按钮(可用按钮):
                结果["操作"] = "跳过"
                结果["人工原因"] = "详情页无操作按钮，可能已被人工处理"
                结果["需要飞书通知"] = False
                return 结果
            return await self._决策_退款(详情, 可用按钮, 规则配置, 队列记录, 结果)

        结果["人工原因"] = f"{售后类型 or '未知类型'}暂不支持自动处理"
        return 结果

    async def _决策_退货退款(
        self,
        详情: dict,
        可用按钮: list[str],
        规则配置: dict,
        结果: dict,
    ) -> dict:
        """退货退款场景优先按页面实际可用按钮处理。"""
        if any("同意退货" in 按钮 for 按钮 in 可用按钮):
            结果["操作"] = "同意退货"
            结果["目标按钮"] = "同意退货"
            结果["需要飞书通知"] = False
            return 结果

        if any("同意退款" in 按钮 for 按钮 in 可用按钮):
            结果["操作"] = "同意退款"
            结果["目标按钮"] = "同意退款"
            结果["需要飞书通知"] = False
            return 结果

        结果["人工原因"] = f"退货退款但无可用操作按钮: {可用按钮}"
        return 结果

    async def _决策_退款(
        self,
        详情: dict,
        可用按钮: list[str],
        规则配置: dict,
        队列记录: dict,
        结果: dict,
    ) -> dict:
        """仅退款场景的决策逻辑。"""
        金额 = self._提取金额(详情.get("退款金额", 0))
        物流状态 = str(详情.get("物流最新状态", "")).strip()
        拒绝次数 = int(队列记录.get("拒绝次数", 0) or 0)
        自动同意上限 = self._提取金额(规则配置.get("自动同意金额上限", 10))
        需要拒绝 = bool(规则配置.get("需要拒绝", False))

        if 金额 <= 自动同意上限:
            目标 = self._找按钮(可用按钮, ["同意退款", "同意拒收后退款"])
            if 目标:
                结果["操作"] = "同意退款"
                结果["目标按钮"] = 目标
                结果["需要备注"] = True
                结果["备注内容"] = "小额自动退款"
                结果["需要飞书通知"] = False
                return 结果

        if bool(详情.get("有售后图片", False)):
            结果["人工原因"] = "有售后图片需人工查看"
            结果["飞书通知内容"] = f"订单{详情.get('订单编号') or 详情.get('订单号')}有售后图片，请人工审核"
            return 结果

        if "拒收" in 物流状态 or "退回" in 物流状态:
            目标 = self._找按钮(可用按钮, ["同意拒收后退款", "同意退款"])
            if 目标:
                结果["操作"] = "同意拒收退款"
                结果["目标按钮"] = 目标
                结果["需要飞书通知"] = False
                return 结果

        if 需要拒绝 and 拒绝次数 < 3:
            目标 = self._找按钮(可用按钮, ["拒绝"])
            if 目标:
                结果["操作"] = "拒绝"
                结果["目标按钮"] = 目标
                结果["弹窗偏好"] = dict(规则配置.get("弹窗偏好") or {})
                结果["需要备注"] = True
                结果["备注内容"] = f"系统拒绝第{拒绝次数 + 1}次"
                结果["需要飞书通知"] = False
                return 结果

        if 需要拒绝 and 拒绝次数 >= 3:
            结果["操作"] = "跳过"
            结果["人工原因"] = "已拒绝3次，不再自动处理"
            结果["飞书通知内容"] = f"订单{详情.get('订单编号') or 详情.get('订单号')}已拒绝3次，请人工跟进"
            return 结果

        结果["人工原因"] = f"退款场景未匹配到规则，金额={金额}"
        结果["飞书通知内容"] = f"订单{详情.get('订单编号') or 详情.get('订单号')}需人工处理，金额{金额}"
        return 结果

    def _有操作按钮(self, 可用按钮: list[str]) -> bool:
        操作词 = ["同意退款", "同意退货", "拒绝", "拒收后退款", "快递拦截"]
        return any(关键词 in 按钮 for 按钮 in 可用按钮 for 关键词 in 操作词)

    def _找按钮(self, 可用按钮: list[str], 优先级列表: list[str]) -> str:
        """按优先级在可用按钮中找第一个匹配项。"""
        for 关键词 in 优先级列表:
            for 按钮 in 可用按钮:
                if 关键词 in 按钮:
                    return 按钮
        return ""


__all__ = ["售后决策引擎"]
