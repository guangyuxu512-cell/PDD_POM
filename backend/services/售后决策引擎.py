"""售后决策引擎。"""
from __future__ import annotations

import re
from typing import Any


class 售后决策引擎:
    """售后决策引擎，负责退货物流判断和退款规则计算。"""

    def __init__(self):
        pass

    @staticmethod
    def _提取金额(值: Any) -> float:
        if isinstance(值, (int, float)):
            return float(值)
        匹配 = re.search(r"(\d+(?:\.\d+)?)", str(值 or ""))
        return float(匹配.group(1)) if 匹配 else 0.0

    def _有操作按钮(self, 可用按钮: list[str]) -> bool:
        操作词 = ["同意退款", "同意退货", "拒绝", "拒收后退款", "快递拦截"]
        return any(关键词 in 按钮 for 按钮 in 可用按钮 for 关键词 in 操作词)

    def _找按钮(self, 可用按钮: list[str], 优先级列表: list[str]) -> str:
        for 关键词 in 优先级列表:
            for 按钮 in 可用按钮:
                if 关键词 in 按钮:
                    return 按钮
        return ""

    async def 决策(self, 详情: dict, 规则配置: dict, 队列记录: dict) -> dict:
        售后类型 = str(详情.get("售后类型", "")).strip()
        可用按钮 = list(详情.get("可用按钮列表") or [])

        结果 = {
            "操作": "人工处理",
            "目标按钮": "",
            "备选按钮": [],
            "弹窗偏好": dict(规则配置.get("弹窗偏好") or {}),
            "需要备注": False,
            "备注内容": "",
            "需要飞书通知": True,
            "飞书通知内容": "",
            "人工原因": "",
            "下次处理天数": None,
        }

        if not self._有操作按钮(可用按钮):
            结果["操作"] = "跳过"
            结果["人工原因"] = "详情页无操作按钮，可能已被人工处理"
            结果["需要飞书通知"] = False
            return 结果

        if "退货退款" in 售后类型:
            return self._决策_退货退款(详情, 可用按钮, 规则配置, 队列记录, 结果)

        if "退款" in 售后类型 and "退货" not in 售后类型:
            return self._决策_退款(详情, 可用按钮, 规则配置, 队列记录, 结果)

        结果["人工原因"] = f"{售后类型 or '未知类型'}暂不支持自动处理"
        return 结果

    def _决策_退货退款(
        self,
        详情: dict,
        可用按钮: list[str],
        规则配置: dict,
        队列记录: dict,
        结果: dict,
    ) -> dict:
        退货物流 = dict(详情.get("退货物流信息") or {})
        有退货物流 = bool(退货物流.get("有退货物流"))
        金额 = self._提取金额(详情.get("退款金额", 0))

        if not 有退货物流:
            if any("同意退货" in 按钮 for 按钮 in 可用按钮):
                结果["操作"] = "同意退货"
                结果["目标按钮"] = "同意退货"
                结果["需要飞书通知"] = False
                return 结果

            结果["操作"] = "等待"
            结果["下次处理天数"] = 3
            结果["人工原因"] = "等待买家寄回"
            结果["需要飞书通知"] = False
            return 结果

        白名单 = list(规则配置.get("退货物流白名单") or [])
        等待时间配置 = dict(
            规则配置.get("退货等待时间")
            or {"刚发出": 3, "中途运输": 1, "到达目的市": 0.25}
        )
        需要入库校验 = bool(规则配置.get("需要入库校验", False))
        自动退款上限 = self._提取金额(规则配置.get("自动退款金额上限", 50))

        所有地区词: list[str] = []
        for 规则 in 白名单:
            所有地区词.extend(list(规则.get("地区关键词") or []))

        轨迹全文 = str(退货物流.get("轨迹全文", ""))
        物流阶段 = self._判断物流阶段(轨迹全文, 所有地区词)

        if 物流阶段 == "刚发出":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间配置.get("刚发出", 3)
            结果["需要飞书通知"] = False
            return 结果

        if 物流阶段 == "中途运输":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间配置.get("中途运输", 1)
            结果["需要飞书通知"] = False
            return 结果

        if 物流阶段 == "到达目的市":
            结果["操作"] = "等待"
            结果["下次处理天数"] = 等待时间配置.get("到达目的市", 0.25)
            结果["需要飞书通知"] = False
            return 结果

        if 物流阶段 in ("派送中", "已签收"):
            匹配结果 = self._匹配白名单(退货物流, 白名单)
            if not 匹配结果["派件人匹配"]:
                订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""
                结果["操作"] = "人工处理"
                结果["人工原因"] = "派件人不在白名单"
                结果["需要备注"] = True
                结果["备注内容"] = "退回派件人不匹配，转人工"
                结果["飞书通知内容"] = f"订单{订单号} 退回派件人不匹配，请人工处理"
                return 结果

            return self._退款判断(
                详情,
                可用按钮,
                需要入库校验,
                自动退款上限,
                金额,
                结果,
            )

        结果["人工原因"] = f"物流状态未知: {轨迹全文[:80]}"
        return 结果

    def _退款判断(
        self,
        详情: dict,
        可用按钮: list[str],
        需要入库校验: bool,
        自动退款上限: float,
        金额: float,
        结果: dict,
    ) -> dict:
        """派件人匹配后的退款决策。"""
        订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""

        if 需要入库校验:
            结果["操作"] = "等待验货"
            结果["需要备注"] = True
            结果["备注内容"] = "退回件已到，待入库校验"
            结果["需要飞书通知"] = True
            结果["飞书通知内容"] = f"订单{订单号} 退回件到达，请入库校验后在前端确认"
            return 结果

        if 金额 <= 自动退款上限:
            目标按钮 = self._找按钮(可用按钮, ["同意退款"])
            if 目标按钮:
                结果["操作"] = "同意退款"
                结果["目标按钮"] = 目标按钮
                结果["需要备注"] = True
                结果["备注内容"] = "退回物流匹配，自动退款"
                结果["需要飞书通知"] = False
                return 结果

        结果["操作"] = "等待验货"
        结果["需要备注"] = True
        结果["备注内容"] = f"退回件已到，金额{金额}元超限{自动退款上限}，待验货"
        结果["需要飞书通知"] = True
        结果["飞书通知内容"] = f"订单{订单号} 金额{金额}超限，请验货后确认"
        return 结果

    def _决策_退款(
        self,
        详情: dict,
        可用按钮: list[str],
        规则配置: dict,
        队列记录: dict,
        结果: dict,
    ) -> dict:
        金额 = self._提取金额(详情.get("退款金额", 0))
        物流状态 = str(详情.get("物流最新状态", "")).strip()
        拒绝次数 = int(队列记录.get("拒绝次数", 0) or 0)
        自动同意上限 = self._提取金额(规则配置.get("自动同意金额上限", 10))
        需要拒绝 = bool(规则配置.get("需要拒绝", False))
        订单号 = 详情.get("订单编号") or 详情.get("订单号") or ""

        if 金额 <= 自动同意上限:
            目标按钮 = self._找按钮(可用按钮, ["同意退款", "同意拒收后退款"])
            if 目标按钮:
                结果["操作"] = "同意退款"
                结果["目标按钮"] = 目标按钮
                结果["需要备注"] = True
                结果["备注内容"] = "小额自动退款"
                结果["需要飞书通知"] = False
                return 结果

        if bool(详情.get("有售后图片", False)):
            结果["人工原因"] = "有售后图片需人工查看"
            结果["需要备注"] = True
            结果["备注内容"] = "有售后图片，转人工审核"
            结果["飞书通知内容"] = f"订单{订单号}有售后图片，请人工审核"
            return 结果

        if "拒收" in 物流状态 or "退回" in 物流状态:
            目标按钮 = self._找按钮(可用按钮, ["同意拒收后退款", "同意退款"])
            if 目标按钮:
                结果["操作"] = "同意拒收退款"
                结果["目标按钮"] = 目标按钮
                结果["需要飞书通知"] = False
                return 结果

        if 需要拒绝 and 拒绝次数 < 3:
            目标按钮 = self._找按钮(可用按钮, ["拒绝"])
            if 目标按钮:
                结果["操作"] = "拒绝"
                结果["目标按钮"] = 目标按钮
                结果["需要备注"] = True
                结果["备注内容"] = f"系统拒绝第{拒绝次数 + 1}次"
                结果["需要飞书通知"] = False
                return 结果

        if 需要拒绝 and 拒绝次数 >= 3:
            结果["操作"] = "跳过"
            结果["人工原因"] = "已拒绝3次，不再自动处理"
            结果["飞书通知内容"] = f"订单{订单号}已拒绝3次，请人工跟进"
            return 结果

        结果["人工原因"] = f"退款场景未匹配到规则，金额={金额}"
        结果["需要备注"] = True
        结果["备注内容"] = f"金额{金额}元未匹配规则，转人工"
        结果["飞书通知内容"] = f"订单{订单号}需人工处理，金额{金额}"
        return 结果

    @staticmethod
    def _判断物流阶段(轨迹全文: str, 目的市关键词: list[str]) -> str:
        if not 轨迹全文:
            return "未知"

        if "签收" in 轨迹全文 or "已签收" in 轨迹全文:
            return "已签收"

        if "派件" in 轨迹全文 or "派送" in 轨迹全文 or "正在为您" in 轨迹全文:
            return "派送中"

        for 关键词 in 目的市关键词:
            if (
                f"到达{关键词}" in 轨迹全文
                or f"到{关键词}" in 轨迹全文
                or f"{关键词}市" in 轨迹全文
            ):
                return "到达目的市"

        if "运输" in 轨迹全文 or "转运" in 轨迹全文 or "发往" in 轨迹全文:
            return "中途运输"

        if "揽收" in 轨迹全文 or "已发出" in 轨迹全文 or "已揽件" in 轨迹全文:
            return "刚发出"

        return "未知"

    @staticmethod
    def _匹配白名单(退货物流: dict, 白名单: list[dict]) -> dict:
        """匹配退货物流是否命中白名单。"""
        快递公司 = str(退货物流.get("退货快递公司", ""))
        轨迹全文 = str(退货物流.get("轨迹全文", ""))
        派件人 = str(退货物流.get("派件人", ""))
        网点 = str(退货物流.get("网点", ""))
        合并文本 = f"{轨迹全文} {派件人} {网点}"

        for 规则 in 白名单:
            规则快递 = str(规则.get("快递公司", "*"))
            if 规则快递 != "*" and 规则快递 not in 快递公司:
                continue

            地区匹配 = any(关键词 in 合并文本 for 关键词 in list(规则.get("地区关键词") or []))
            派件人匹配 = any(名字 in 合并文本 for 名字 in list(规则.get("派件人") or []))
            if 地区匹配 or 派件人匹配:
                return {
                    "地区匹配": 地区匹配,
                    "派件人匹配": 派件人匹配,
                    "匹配的规则": 规则,
                }

        return {"地区匹配": False, "派件人匹配": False, "匹配的规则": None}


__all__ = ["售后决策引擎"]
